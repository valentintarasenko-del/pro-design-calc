// Логика расчёта стоимости мебели

// Форматирование суммы в рубли
export const fmt = (n) =>
  new Intl.NumberFormat('ru-RU').format(Math.round(n || 0)) + ' ₽';

// Расчёт количества листов ЛДСП
// пеналы: каждый = 1 лист
// базы: (нижняя + верхняя) / коэффициент, округляем вверх
export function calcSheets(нижняя, верхняя, пеналы, коэф = 1.6) {
  const базовыеЛисты = Math.ceil((нижняя + верхняя) / коэф);
  return пеналы + базовыеЛисты;
}

// Полный расчёт по всем блокам
export function calcTotal(data, settings) {
  const нижняя = parseFloat(data.нижняя) || 0;
  const верхняя = parseFloat(data.верхняя) || 0;
  const пеналы = parseInt(data.пеналы) || 0;

  // Корпуса
  const листы = (нижняя > 0 || верхняя > 0 || пеналы > 0)
    ? calcSheets(нижняя, верхняя, пеналы, settings.коэф)
    : 0;
  const корпуса = листы * (settings.ценаЛиста || 0);

  // Фасады: сумма всех позиций
  const фасадыСумма = (data.фасады || []).reduce((sum, f) => {
    const площадь = parseFloat(f.площадь) || 0;
    const цена = parseFloat(f.цена) || 0;
    return sum + площадь * цена;
  }, 0);

  // Фрезеровка
  const фрезеровка = data.фрезеровкаВкл
    ? (parseFloat(data.фрезеровкаОбъём) || 0) * (parseFloat(data.фрезеровкаЦена) || 0)
    : 0;

  const столешница = parseFloat(data.столешница) || 0;
  const фурнитура = parseFloat(data.фурнитура) || 0;

  // Итого по мебели (без монтажа и доставки)
  const итогоМебель = корпуса + фасадыСумма + фрезеровка + столешница + фурнитура;

  // Монтаж считается от суммы мебели
  const монтажПроцент = parseFloat(data.монтажПроцент) || 0;
  const монтаж = итогоМебель * (монтажПроцент / 100);

  const доставка = parseFloat(data.доставка) || 0;

  const итого = итогоМебель + монтаж + доставка;

  // Дополнительная наценка поверх итога
  let итогоСНаценкой = итого;
  const допНаценка = parseFloat(data.допНаценка) || 0;
  if (data.допНаценкаТип === 'percent' && допНаценка > 0) {
    итогоСНаценкой = итого * (1 + допНаценка / 100);
  } else if (data.допНаценкаТип === 'sum' && допНаценка > 0) {
    итогоСНаценкой = итого + допНаценка;
  }

  // Маржа (только если заполнена себестоимость)
  const себестоимость = parseFloat(data.себестоимость) || null;
  const маржа = себестоимость ? итогоСНаценкой - себестоимость : null;
  const маржаПроцент = маржа && итогоСНаценкой > 0
    ? (маржа / итогоСНаценкой * 100).toFixed(1)
    : null;

  return {
    листы,
    корпуса,
    фасады: фасадыСумма,
    фрезеровка,
    столешница,
    фурнитура,
    итогоМебель,
    монтаж,
    монтажПроцент,
    доставка,
    итого,
    итогоСНаценкой,
    себестоимость,
    маржа,
    маржаПроцент,
  };
}
